

<template>
    <div id="suggest-form" class="form">
            <section class="form__container">
              <h2 class="form__header">Suggest a Translation ðŸ“˜</h2>
              <p class="form__paragraph">Please follow the format of the input placeholders</p>
              <div>
                <form ref="form" @submit.prevent.once="sendEmail">
                  <label class="form__label">Traditional</label><br>
                  <input name="traditional" @input="setTraditional" :value="traditional" type="text" class="form__input" placeholder="é›»è…¦" required><br>
                  <label class="form__label">Simplified</label><br>
                  <input name="simplified" @input="setSimplified" :value="simplified" type="text" class="form__input" placeholder="ç”µè„‘" required><br>
                  <label class="form__label">Pinyin</label><br>
                  <input name="pinyin" @input="setPinyin" :value="pinyin" type="text" class="form__input" placeholder="DiÃ nnÇŽo" required><br>
                  <label class="form__label">English</label><br>
                  <input name="english" @input="setEnglish" :value="english" type="text" class="form__input" placeholder="computer" required><br>
                  <p class="form__error-message" v-if="error">{{ message }}</p>
                  
                  <button type="submit" class="form__btn">Suggest</button>
                </form>
              </div>
              
            </section>
          </div>          
</template>

<script>
import useVuelidate from '@vuelidate/core'
import { required } from '@vuelidate/validators'
import emailjs from '@emailjs/browser'

export default {
  setup () {
    return {
      v$: useVuelidate()
    }
  },
  data () {
    return {
      traditional: '',
      simplified: '',
      pinyin: '',
      english: '',
    }
  },
  validations () {
    return {
      traditional: { required },
      simplified: { required},
      pinyin: { required },
      english: { required }
    }
  },
  errors () {
      return {
          error: false,
          message: ''
      }
  },
  methods: {
    checkForLatinChars ($event) {
        const latinCharacters = /^[A-Za-z0-9]*$/.test($event.target.value)
        if (!latinCharacters) {
            this.error = true
            this.message = 'Please only type latin characters in the pinyin and English inputs'
        }
    },
    checkAgainstLatinChars ($event) {
        const latinCharacters = /^[A-Za-z0-9]*$/.test($event.target.value)
        if (latinCharacters) {
            this.error = true
            this.message = `Please don't type latin characters in the Chinese inputs`
            console.log(this.message)
        }
        else if ($event.target.value === '') {
            this.error = false
            this.message = ''
        }
    },
    falsifyError (input) {
        if (input === '') {
          this.error = false
      }
    },
    setTraditional ($event) {
      this.traditional = $event.target.value
      this.v$.traditional.$touch()
      this.checkAgainstLatinChars($event)
      this.falsifyError(this.traditional)
    },
    setSimplified ($event) {
      this.simplified = $event.target.value
      this.v$.simplified.$touch()
      this.checkAgainstLatinChars($event)
      this.falsifyError(this.simplified)
    },
    setPinyin ($event) {
      this.pinyin = $event.target.value
      this.v$.pinyin.$touch()
      this.checkForLatinChars($event)
      this.falsifyError(this.pinyin)
    },
    setEnglish ($event) {
      this.english = $event.target.value
      this.v$.english.$touch()
      this.checkForLatinChars($event)
      this.falsifyError(this.english)
    },
    sendEmail () {
      emailjs.sendForm('service_tfsl6fy', 'template_y9i14r9', this.$refs.form, 'MSRjmXN5q5SR9tq8I')
        .then((result) => {
            console.log('SUCCESS!', result.text);
        }, (error) => {
            console.log('FAILED...', error.text);
        });
    }
  }
} 
</script>

